'''Calcular la media de tres valores introducidos por teclado'''

val1 = int(input('Introduzca primera nota: '))
val2 = int(input('Introduzca segunda nota: '))
val3 = int(input('Introduzca tercera nota: '))

resultado = val1+val2+val3
media = resultado / 3

print('La media de las tres notas es: ', media)